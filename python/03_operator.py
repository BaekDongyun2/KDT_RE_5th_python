# 산술 연산
# 문자열 연결: + 연산자 --> 문자열을 이어 붙이는 기능을 함
# 문자열 반복: * 연산자 --> 문자열을 곱하는 정수만큼 반복함
# 문자열, 숫자간 다허기 및 나누기는 연산 불가

#--------------------
# 문자열 연산
#--------------------

# +: 문자열 결합
print(100 + 200) # 300
print("100" + "200") # 100200

# *: 문자열 반복
hi = "Hi!"
print(hi * 3)

# /, - 등은 지원하지 않는다.

intro = "둠칫"
drop = "두둠칫"
print(intro + intro + intro + (intro + drop + intro + drop) * 5)