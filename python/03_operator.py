# - 사칙연산을 포함한 수학 연산 수행
# - +, -, *, /,  //, %, **

# 비교 연산자
# - 두 값을 비교하는 연산 수행
# - 결과는 True or False 반환
# - ==, !=, >, <, >=, <=

# 논리 연산자
# - 논리에 대한 연산 수행 (True, False)
